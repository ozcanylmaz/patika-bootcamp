package week1;

public class AritmetikIslem {
        public static void main(String[] args) {
            // Sabit sayılar
            int A = 10;
            int B = 2;
            int C = 3;

            // İşlem: A + B * C - B
            int sonuc = A + B * C - B;

            // Sonucu yazdır
            System.out.println("İşlem: A + B * C - B");
            System.out.println("A = " + A + ", B = " + B + ", C = " + C);
            System.out.println("İşlemin sonucu: " + sonuc);
        }
    }

